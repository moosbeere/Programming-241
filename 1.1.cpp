#include <iostream> // Подключение библиотеки для стандартного ввода и вывода
#include <cstdint>  // Подключение библиотеки для работы с типами фиксированной ширины (uint8_t и т.д.)
using namespace std; // Использование стандартного пространства имен

int main() {
    setlocale(LC_ALL, "Russian"); // Поддержка русского языка в консоли

    int a = 150;        // Объявление целого числа (обычно 4 байта)
    float b = 15.933f;  // Объявление дробного числа (f в конце означает тип float)
    uint8_t c = 250;    // Объявление беззнакового целого размером 1 байт (от 0 до 255)

    cout << "a = " << a << endl; // Вывод переменной a
    cout << "b = " << b << endl; // Вывод переменной b
    // static_cast<int>(c) нужен, чтобы uint8_t вывелся как число, а не как символ ASCII
    cout << "c = " << static_cast<int>(c) << endl;

    uint8_t day = 27;           // День рождения (целое число 1 байт)
    string month = "Магеррамли"; // Строка для хранения названия месяца или фамилии
    uint16_t year = 2007;       // Год рождения (беззнаковое целое 2 байта)

    // Вывод даты рождения в одну строку
    cout << "Моя дата рождения: " << static_cast<int>(day) << " " << month << " " << year << " года" << endl;

    const float PI = 2.3f;      // Объявление константы (нельзя изменить в коде)
    const string OS = "WINDOWS"; // Объявление строковой константы

    cout << PI << " " << OS << endl; // Вывод констант через пробел

    return 0; // Завершение программы с кодом 0 (успешно)
}