#include <iostream> // Подключение библиотеки для ввода и вывода (cout, cin)
using namespace std; // Использование стандартного пространства имен

int main() {
    setlocale(LC_ALL, "Russian"); // Поддержка русского языка в консоли
    double num1, num2; // Объявление двух переменных для дробных чисел
    char op; // Объявление переменной для хранения символа операции (+, -, *, /)

    cout << "Введите первое число: "; // Вывод текста в консоль
    cin >> num1; // Чтение первого числа с клавиатуры
    cout << "Введите второе число: "; // Вывод текста
    cin >> num2; // Чтение второго числа

    // Часть со средним арифметическим
    double average = (num1 + num2) / 2.0; // Вычисление среднего (сумма делить на 2)
    cout << "Среднее арифметическое этих чисел: " << average << endl; // Вывод результата среднего

    cout << "Введите операцию (+, -, *, /): "; // Просьба ввести символ операции
    cin >> op; // Чтение символа операции

    switch (op) { // Начало оператора выбора (сравнение переменной op)
    case '+': // Если введено '+'
        cout << "Сумма: " << (num1 + num2) << endl; // Складываем и выводим
        break; // Выход из switch
    case '-': // Если введено '-'
        cout << "Разность: " << (num1 - num2) << endl; // Вычитаем и выводим
        break; // Выход из switch
    case '*': // Если введено '*'
        cout << "Произведение: " << (num1 * num2) << endl; // Умножаем и выводим
        break; // Выход из switch
    case '/': // Если введено '/'
        if (num2 != 0) { // Проверка: нельзя делить на ноль
            cout << "Частное: " << (num1 / num2) << endl; // Делим и выводим
        }
        else { // Если второе число — ноль
            cout << "Ошибка: деление на ноль!" << endl; // Сообщаем об ошибке
        }
        break; // Выход из switch
    default: // Если введен любой другой символ
        cout << "Неизвестная операция!" << endl; // Сообщаем о некорректном вводе
    }

    return 0; // Завершение программы
}