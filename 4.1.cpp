#include <iostream> // Библиотека ввода-вывода
#include <string>   // Библиотека для работы со строками
using namespace std; // Использование стандартного пространства имен

class Weapon { // Объявление класса "Оружие"
private: // Секция закрытых данных (доступны только внутри класса)
    string name;   // Название оружия
    int damage;    // Урон
    double weight; // Вес
public: // Секция открытых данных и методов
    // Конструктор с параметрами для инициализации полей
    Weapon(const string& n, int d, double w)
        : name(n), damage(d), weight(w) { // Список инициализации
    }

    // Конструктор по умолчанию (вызывает основной конструктор с готовыми значениями)
    Weapon() : Weapon("Балка", 2, 1.5) {}

    // Метод для вывода информации об объекте
    void printInfo() const {
        cout << "Название оружия: " << name << endl;
        cout << "Наносимый урон: " << damage << endl;
        cout << "Вес оружия: " << weight << endl;
    }
};

int main() {
    setlocale(LC_ALL, "Russian"); // Поддержка русского языка

    // Создание объекта sword с конкретными значениями
    Weapon sword("Клинок огня", 42, 3.1);

    // Создание объекта stick (используется конструктор по умолчанию)
    Weapon stick;

    cout << "=== Первый объект (создан с параметрами): ===" << endl;
    sword.printInfo(); // Вызов метода вывода для меча
    cout << endl;

    cout << "=== Второй объект (создан без параметров): ===" << endl;
    stick.printInfo(); // Вызов метода вывода для палки

    return 0; // Завершение программы
}