#include <iostream> //Для ввода-вывода
#include <vector> //Для vector
#include <string> //Для string

//Сортировки выбором
void Sort_viborom(std::vector<std::string>& phones) {
    int n = phones.size(); //Количество телефонов
    for (int i = 0; i < n - 1; i++) { //Проходим по всем элементам кроме последнего
        int minIndex = i; //Индекс минимального элемента - текущий i
        for (int j = i + 1; j < n; ++j) {  // Ищем минимальный элемент в оставшейся части массива
            if (phones[j] < phones[minIndex]) { //Если текущий элемент меньше найденного минимума, то обновинть минимальный элемент
                minIndex = j;    
            }
        }
        if (minIndex != i) {       // Если минимальный элемент не равен текущему, тогда поменяем местами текущий и минимальный элемент
            std::swap(phones[i], phones[minIndex]);  
        }
    }
}

//Создаем строки с номерами телефонов
int main() {
    setlocale(LC_ALL, "ru"); //На русском языке

    std::vector<std::string> phones = {   
        "26-10-06",
        "11-04-13",
        "30-10-85",
        "09-07-86",
        "04-03-10"
    };

    Sort_viborom(phones); //Вызываем функцию сортировки выбором

    std::cout << "Отсортированный список телефонов:\n"; //Вывод заголовка
    for (const auto& phone : phones) { //Проходимся по всем отсортированным телефонам
        std::cout << phone << "\n"; //Вывод телефона на отдельных строчках
    }

    return 0;   
}
