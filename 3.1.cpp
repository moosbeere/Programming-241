#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "Russian");
    // 1. Создайте переменную со значением на Ваше усмотрение.
    int myVar = 42;

    // 2. Объявите указатель на созданную переменную.
    int* ptr = &myVar;

    // 3. Выведите в консоль адрес указателя и значение, которое хранится по этому адресу.
    cout << "Адрес myVar (через ptr): " << ptr << endl;
    cout << "Значение myVar (через ptr): " << *ptr << endl;

    // 4. Измените значение, которое хранится по адресу указателя.
    *ptr = 100;

    // 5. Убедитесь, что переменная из 1-го пункта так же изменила своё значение.
    cout << "Новое значение myVar (после изменения через указатель): " << myVar << endl;

    // 6. Создайте массив из нескольких элементов (тип и значение на Ваше усмотрение).
    double arr[5] = { 1.5, 2.5, 3.5, 4.5, 5.5 };

    // 7. Выведите все элементы массива в консоль, используя указатель.
    double* arrPtr = arr;
    cout << "Все элементы массива: ";
    for (int i = 0; i < 5; ++i) {
        cout << *(arrPtr + i) << " ";
    }
    cout << endl;

    // 8. Объявите целую переменную со значением 5.
    int value = 5;

    // 9. Создайте такой указатель на эту переменную, чтобы нельзя было менять адрес, на который он ссылается,
    //    но при этом, чтобы через него можно было менять значение.
    int* const pValue = &value;

    // Для демонстрации - создадим еще одну переменную
    int anotherValue = 10;

    // 10. Убедитесь, что Вы всё сделали правильно...
    // Попытка поменять адрес (закомментировано, чтобы не вызывать ошибку компиляции)
    // Раскомментируйте следующую строку для проверки ошибки:
    // pValue = &anotherValue;  // ОШИБКА: Cannot assign to variable 'pValue' with const-qualified type 'int *const'

    // Меняем значение через указатель (это разрешено)
    *pValue = 123;

    cout << "Новое значение value (после изменения через pValue): " << value << endl;

    // Дополнительная проверка:
    cout << "Дополнительная проверка - обращение через указатель: " << *pValue << endl;

    // Выведем адрес для наглядности
    cout << "Адрес, на который указывает pValue: " << pValue << endl;

    return 0;
}
