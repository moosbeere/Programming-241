#include <iostream>
#include <locale>

using namespace std;

// Функция включения поддержки русского языка
void enableRussian() {
    setlocale(LC_ALL, "");
}

int main() {
    enableRussian(); // ✅ русский язык в консоли

    // 1. Переменная с произвольным значением
    int value = 10;

    // 2. Указатель на переменную
    int* ptr = &value;

    // 3. Вывод адреса и значения по адресу
    cout << "Адрес указателя: " << ptr << endl;
    cout << "Значение по адресу указателя: " << *ptr << endl;

    // 4. Изменение значения по адресу указателя
    *ptr = 25;

    // 5. Проверка изменения исходной переменной
    cout << "Новое значение переменной value: " << value << endl;

    cout << "-----------------------------" << endl;

    // 6. Массив из нескольких элементов
    int arr[5] = { 1, 2, 3, 4, 5 };

    // 7. Вывод элементов массива через указатель
    int* arrPtr = arr; // имя массива — указатель на первый элемент

    cout << "Элементы массива: ";
    for (int i = 0; i < 5; i++) {
        cout << *(arrPtr + i) << " ";
    }
    cout << endl;

    cout << "-----------------------------" << endl;

    // 8. Целая переменная со значением 5
    int number = 5;

    // 9. Указатель, у которого нельзя менять адрес,
    // но можно менять значение
    int* const constPtr = &number;

    // Изменяем значение через указатель
    *constPtr = 15;

    // 10. Проверка
    cout << "Новое значение переменной number: " << number << endl;

    // Попытка изменить адрес (раскомментировать для ошибки компиляции)
    // int other = 100;
    // constPtr = &other; // ❌ ошибка компилятора

    return 0;
}
