#include <iostream> // Подключение библиотеки для ввода и вывода
using namespace std; // Использование стандартного пространства имен

int main() {
    setlocale(LC_ALL, "Russian"); // Поддержка русского языка в консоли
    int n; // Переменная для хранения введенного числа
    while (true) { // Бесконечный цикл для проверки ввода
        cout << "Введите целое положительное число: "; // Вывод просьбы
        cin >> n; // Чтение числа
        if (n > 0) { // Если число положительное
            break; // Выход из цикла
        }
        cout << "Ошибка: введите положительное число!" << endl; // Сообщение при неверном вводе
    }

    int sum = 0; // Переменная для накопления суммы
    cout << "Сумма чисел от 1 до " << n << ": "; // Начало вывода строки суммы
    for (int i = 1; i <= n; ++i) { // Цикл от 1 до n включительно
        sum += i; // Прибавляем i к общей сумме
        cout << i; // Выводим текущее число
        if (i != n) cout << " + "; // Если это не последнее число, выводим плюс
    }
    cout << " = " << sum << endl; // Вывод итогового результата суммы

    int arr[10] = { 3, 7, 2, 9, 14, 5, 10, 21, 8, 1 }; // Создание массива из 10 чисел

    cout << "Все элементы массива: "; // Текст перед выводом массива
    for (int i = 0; i < 10; ++i) { // Цикл для прохода по всем индексам (0-9)
        cout << arr[i]; // Вывод элемента массива по текущему индексу
        if (i != 9) cout << " "; // Добавляем пробел между числами (кроме последнего)
    }
    cout << endl; // Перенос строки

    cout << "Числа на чётных позициях: "; // Позиции 0, 2, 4, 6, 8 (индексы)
    for (int i = 0; i < 10; ++i) { // Проход по массиву
        if (i % 2 == 0) { // Проверка индекса на чётность
            cout << arr[i] << " "; // Вывод элемента на чётном индексе
        }
    }
    cout << endl; // Перенос строки

    int sum_odd_pos = 0; // Переменная для суммы чисел на нечётных позициях
    for (int i = 0; i < 10; ++i) { // Проход по массиву
        if (i % 2 != 0) { // Проверка индекса на нечётность (1, 3, 5, 7, 9)
            sum_odd_pos += arr[i]; // Прибавление значения к сумме
        }
    }
    cout << "Сумма элементов на нечётных позициях: " << sum_odd_pos << endl; // Вывод итога

    return 0; // Завершение программы
}