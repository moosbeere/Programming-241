#include <iostream> // Подключение библиотеки ввода-вывода
#include <string>   // Подключение библиотеки для работы со строками
using namespace std; // Использование стандартного пространства имен

// Функция вычисления суммы чисел от 1 до n (n=1 — параметр по умолчанию)
int sum_upto(int n = 1) {
    if (n <= 0) return 0; // Если число меньше или равно 0, возвращаем 0
    int s = 0; // Переменная для суммы
    for (int i = 1; i <= n; ++i) s += i; // Цикл накопления суммы
    return s; // Возврат результата
}

// Функция, возвращающая два значения через ссылки (sum и prod)
void calculate(int a, int b, int& sum, int& prod) {
    sum = a + b; // Записываем результат сложения в переменную по ссылке
    prod = a * b; // Записываем результат умножения в переменную по ссылке
}

int main() {
    setlocale(LC_ALL, "Russian"); // Поддержка русского языка в консоли

    // 1. Двумерный массив 2x3 (2 строки, 3 столбца)
    int arr2d[2][3] = { {1, 4, 7}, {2, 5, 8} };

    // 2. Сумма всех чисел массива
    int sum_all = 0; // Переменная для общей суммы
    for (int i = 0; i < 2; ++i) // Цикл по строкам
        for (int j = 0; j < 3; ++j) // Вложенный цикл по столбцам
            sum_all += arr2d[i][j]; // Прибавление каждого элемента
    cout << "Сумма всех чисел двумерного массива: " << sum_all << endl;

    // 3. Одномерный массив из 3-х элементов
    int arr1d[3];

    // 4. Суммы столбцов двумерного массива
    for (int j = 0; j < 3; ++j) { // Проход по столбцам
        arr1d[j] = arr2d[0][j] + arr2d[1][j]; // Складываем элементы в столбце
    }

    // 5. Вывод одномерного массива
    cout << "Суммы по столбцам: ";
    for (int j = 0; j < 3; ++j) // Цикл вывода элементов
        cout << arr1d[j] << " ";
    cout << endl;

    // 6. Переменная float (число с плавающей точкой)
    float fval = 20.84;

    // 7. Две ссылки на одну и ту же переменную fval
    float& ref1 = fval;
    float& ref2 = fval;

    // 8. Изменение значения fval через первую ссылку
    ref1 = 42.77;

    // 9. Проверка: изменение через ссылку меняет оригинал и все привязанные ссылки
    cout << "fval: " << fval << endl;
    cout << "ref1: " << ref1 << endl;
    cout << "ref2: " << ref2 << endl;

    // 10-11. Вызов функции с аргументом и без (используется n=1)
    cout << "sum_upto(5): " << sum_upto(5) << endl;
    cout << "sum_upto(): " << sum_upto() << endl;

    // 12-14. Работа с функцией через передачу по ссылке
    int x = 3, y = 4, s = 0, p = 0; // Исходные данные
    calculate(x, y, s, p); // s и p изменятся внутри функции
    cout << "Сумма: " << s << ", Произведение: " << p << endl;

    // 15. Переменная строкового типа
    string str = "Пример строки";
    cout << str << endl;

    // 16-18. Вложенные циклы и область видимости
    for (int i = 0; i < 3; ++i) { // Внешний цикл
        int inside_val = i * 10; // Локальная переменная (создается на каждой итерации)
        cout << "arr1d[0] внутри цикла: " << arr1d[0] << endl;
        for (int k = 0; k < 3; ++k) // Внутренний цикл
            cout << "inside_val: " << inside_val << endl;
    }

    // 19. Ошибка: inside_val не существует вне блока цикла for
    // cout << inside_val << endl;

    return 0; // Завершение программы
}