#include <iostream>   // Библиотека для ввода и вывода данных
using namespace std;  // Позволяет использовать cout, cin без std::

int main() {
    double num1, num2; // Переменные для хранения двух вещественных чисел
    char op;           // Переменная для хранения символа операции

    // Ввод первого числа
    cout << "Введите первое число: ";
    cin >> num1;

    // Ввод второго числа
    cout << "Введите второе число: ";
    cin >> num2;

    // Вычисление среднего арифметического двух чисел
    // Складываем числа и делим на 2.0, чтобы получить вещественный результат
    double average = (num1 + num2) / 2.0;
    cout << "Среднее арифметическое этих чисел: " << average << endl;

    // Запрос математической операции
    cout << "Введите операцию (+, -, *, /): ";
    cin >> op;

    // Выполнение выбранной операции
    switch(op) {
        case '+':  // Сложение
            cout << "Сумма: " << (num1 + num2) << endl;
            break;

        case '-':  // Вычитание
            cout << "Разность: " << (num1 - num2) << endl;
            break;

        case '*':  // Умножение
            cout << "Произведение: " << (num1 * num2) << endl;
            break;

        case '/':  // Деление
            // Проверка деления на ноль
            if (num2 != 0) {
                cout << "Частное: " << (num1 / num2) << endl;
            } else {
                cout << "Ошибка: деление на ноль!" << endl;
            }
            break;

        default:   // Если введена неизвестная операция
            cout << "Неизвестная операция!" << endl;
    }

    return 0; // Завершение программы
}
