#include <iostream>
using namespace std;

int main() {

    setlocale(LC_ALL, "Russian");

    // 1. Запрос целого положительного числа с проверкой
    int number;
    while (true) {
        cout << "Введите целое положительное число: ";
        cin >> number;

        if (number > 0) {
            break;  // Выходим из цикла, если число положительное
        }
        else {
            cout << "Ошибка: число должно быть положительным! Попробуйте снова.\n";
        }
    }

    // 2. Вывод суммы всех чисел от 1 до введённого числа
    int sum = 0;
    cout << "Сумма чисел от 1 до " << number << ": ";
    for (int i = 1; i <= number; i++) {
        sum += i;
        cout << i;
        if (i < number) {
            cout << " + ";
        }
    }
    cout << " = " << sum << "\n\n";

    // 3. Создание массива из 10 чисел
    int arr[10] = { 5, 12, 8, 23, 17, 9, 31, 14, 6, 19 };

    // 4. Вывод всех 10 чисел массива
    cout << "Массив из 10 чисел: ";
    for (int i = 0; i < 10; i++) {
        cout << arr[i];
        if (i < 9) {
            cout << " "; // чтоб после каждого числа шел пробел и вывод не был слитным иначе вывод будет 512823179.....
        }
    }
    cout << "\n";

    // 5. Вывод чисел на чётных позициях (0, 2, 4, 6, 8)
    cout << "Числа на чётных позициях: ";
    for (int i = 0; i < 10; i++) {
        if (i % 2 == 0) {  // Проверка на чётность позиции
            cout << arr[i] << " ";
        }
    }
    cout << "\n";

    // 6. Вывод суммы элементов на нечётных позициях (1, 3, 5, 7, 9)
    int oddSum = 0;
    cout << "Сумма чисел на нечётных позициях: ";
    for (int i = 0; i < 10; i++) {
        if (i % 2 != 0) {  // Проверка на нечётность позиции
            oddSum += arr[i];
            cout << arr[i];
            if (i < 9) {
                cout << " + ";
            }
        }
    }
    cout << " = " << oddSum << "\n";

    return 0;
}